    private void btnAgregarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAgregarMouseClicked
        String nombre=txtNombre.getText();
        String carrera=(String)cboCarrera.getSelectedItem();
        String semestre=(String)cboSemestre.getSelectedItem();
        String fechaRegistro=formato.format(fchRegistro.getDate());
        Tutoria a=new Tutoria(nombre,carrera,semestre,fechaRegistro);
        bd.registrarTutoria(a);
        modeloTutorias.agregarTutoria(a);
        txtNombre.setText("");
        fchRegistro.setDateFormatString("");
    }//GEN-LAST:event_btnAgregarMouseClicked

